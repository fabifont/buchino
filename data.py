def get_regions():
  regions_list = []
  for region in regions:
    regions_list.append(list(region.keys())[0])
  return regions_list


def get_postal_codes(_region):
  postal_codes_list = []
  for region in regions:
    if _region == list(region.keys())[0]:
      for country in list(region.values())[0]:
        for postal_code in list(country.values())[0]:
          if postal_code not in postal_codes_list:
            postal_codes_list.append(postal_code)
      break
  return postal_codes_list


def get_countries(_region, _postal_code):
  countries_list = []
  for region in regions:
    if _region == list(region.keys())[0]:
      for country in list(region.values())[0]:
        for postal_code in list(country.values())[0]:
          if postal_code == _postal_code:
            countries_list.append(list(country.keys())[0])
      break
  return countries_list
